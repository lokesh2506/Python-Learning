s="lokesh"

print(s[1])  # O

print(s[-1]) # h

print(s[1:4]) # oke  start index 1 end index before 4

print(s[1:4:2]) #oe start index 1 end index before 4 and increment by 2

print(s[::-1]) # orint inreverse hsekol

print(-4,-1) # kes

print(len(s)) #length of string

print(s.upper()) #string into upper case

print(s.lower()) #string into lower case

print(s.capitalize()) #first leeter of string only caps

print(s.title())  #first letter of a each word become caps

print(s.swapcase())  #upper -> lower  lower -> upper

print(s.strip()) # remove the white spaces from the start and end of the string

print(s.lstrip()) # remove the white spaces from the left side of the string

print(s.rstrip()) #remove the white spaces form the right side of the string

print(s.islower)
print(s.isupper())

print(s.isalnum())  # str is a alpha numeric or not

print(s.isalpha()) # str is a alphabetic or not

print(s.isdigit()) # str is a numeric or not